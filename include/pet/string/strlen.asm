str_len: .byte 0

// StrLen(instring) — sets str_len = length of null-terminated string
.macro StrLen(instring) {
    ldx #$ff
    stx str_len
sl_loop:
    inx
    lda instring, x
    bne sl_loop
    stx str_len
}
